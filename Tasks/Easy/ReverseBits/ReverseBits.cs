namespace Tasks.Easy.ReverseBits
{
    public static class ReverseBits
    {
        public static uint ReverseBitsMethod(uint n)
        {
            n = (n >> 16) | (n << 16);
            n = ((n & 0xff00ff00) >> 8) | ((n & 0x00ff00ff) << 8); //11111111000000001111111100000000
            n = ((n & 0xf0f0f0f0) >> 4) | ((n & 0x0f0f0f0f) << 4); //11110000111100001111000011110000 //1111000011110000111100001111
            n = ((n & 0xcccccccc) >> 2) | ((n & 0x33333333) << 2); //11001100110011001100110011001100 //110011001100110011001100110011
            n = ((n & 0xaaaaaaaa) >> 1) | ((n & 0x55555555) << 1); //10101010101010101010101010101010 //1010101010101010101010101010101

            return n;
        }
    }
}
